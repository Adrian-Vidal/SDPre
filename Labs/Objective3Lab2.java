public class Objective3Lab2 {
	public static void main(String[] args) {

		double side1 = 5;
		double side2 = 11;
		double side1squared = (side1 * side1);
		double side2squared = (side2 * side2);
		double hypotenuse = Math.sqrt(side1squared + side2squared);

		System.out.println("The hypotenuse of a triangle with sides " + side1 + " and " + side2 + " is " + hypotenuse);






	}
}